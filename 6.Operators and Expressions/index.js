console.log(" Arithmetic Operators in JS")
let a=45
let b=4
console.log("a + b = ",a+b)
console.log("a - b = ",a-b)
console.log("a / b = ",a/b)
console.log("a * b = ",a*b)
console.log("a ** b = ",a**b)
console.log("a % b = ",a%b)
console.log("a ++ = ",a++)
console.log("a  = ",a)
console.log("a -- = ",a--)
console.log("a  = ",a)  //45
console.log("++a  = ",++a)  //46
console.log("--a  = ",--a)  //45


